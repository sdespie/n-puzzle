#include "libft.h"

int	ft_sqrt(int n)
{
	int ret;

	ret = 2;
	if (n <= 0)
		return (0);
	if (n == 1)
		return (1);
	while (ret <= n / (ret - 1))
	{
		if (ret * ret == n)
			return (ret);
		ret++;
	}
	return (0);
}
