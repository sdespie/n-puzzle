#include "libft.h"

int			ft_atoi(const char *s)
{
	unsigned long	res;
	int				neg;

	res = 0;
	neg = 1;
	while (*s && (*s == '\t' || *s == '\n' || *s == '\f' || *s == '\r'
				|| *s == '\v' || *s == ' '))
		s++;
	if (*s == '-')
	{
		s++;
		neg = -1;
	}
	else if (*s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		res = res * 10 + ((*s++) - '0');
		if (res > 9223372036854775807)
			return (neg < 0 ? 0 : -1);
	}
	return (res * neg);
}
